# 生成CSV文件
csv_file = "/Users/bytedance/Desktop/1/1/testStore/水果价格表.csv"

with open(csv_file, 'w', newline='', encoding='utf-8-sig') as f:
    writer = csv.writer(f)
    writer.writerow(headers)
    
    for row in fruit_data:
        category, name, cost, price8, price12, price18 = row
        profit8 = price8 - cost
        profit12 = price12 - cost
        profit18 = price18 - cost
        writer.writerow([category, name, cost, price8, price12, price18, profit8, profit12, profit18])

